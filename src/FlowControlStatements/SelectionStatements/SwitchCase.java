package FlowControlStatements.SelectionStatements;

public class SwitchCase {
    public static void main(String[]args){
        int a = 300;
        switch (a)
        {
            case 10: System.out.println("Sachin");
            break;

            case 20: System.out.println("Sneha");
            break;

            case 30: System.out.println("Atharva");
            break;

            default: System.out.println("Khanvilkar");
            break;
        }
    }
}

