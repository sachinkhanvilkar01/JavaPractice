package FlowControlStatements.SelectionStatements;

public class If {

    /* Syntax
    if(condition)
    { true body
    }
    only if means true body will be executed if false then nothing executed
    */

    public static void main(String [] args){
        int a =1000, b=200;
        if(a>b)
        {
            System.out.println("a is less than b");
        }

    }

}
