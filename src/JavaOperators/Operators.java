package JavaOperators;

public class Operators {

    public static void main (String[]args){

        System.out.println(10&7);
        System.out.println(4&6);
        System.out.println(2&4);

        System.out.println(10>20 & 30<40);
        System.out.println(10>20 | 30<40);

        System.out.println(10<20 && 30<40);
    }
}
